package hotel.soap;

public class HotelRoom {

    private String type;

    private float price;


    public HotelRoom() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}
